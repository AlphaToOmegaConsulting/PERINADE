        '--image', str(ROOT / j['image']),
        '--use-case', j['use_case'],
        '--prompt', j['prompt'],
        '--scene', j['scene'],
        '--subject', j['subject'],
        '--style', j['style'],
        '--composition', j['composition'],
        '--lighting', j['lighting'],
        '--constraints', j['constraints'],
